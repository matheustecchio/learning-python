#%%
# Arithmetic Operators

x = 20
y = 10

o1 = x +  y   # Addition
o2 = x -  y   # Subtraction
o3 = x *  y   # Multiplication
o4 = x /  y   # Division
o5 = x %  y   # Modulus
o6 = x ** y  # Exponentiation
o7 = x // y  # Floor division

print(o1)
print(o2)
print(o3)
print(o3)
print(o4)
print(o5)
print(o6)
print(o7)

#%%
# Comparison Operators

x = 20
y = 10

print(x == y)   # Equal
print(x != y)   # Not equal
print(x < y)    # Greater than
print(x > y)    # Less than
print(x <= y)   # Greater than or equal to
print(x >= y)   # Less than or equal to

#%% 
# Boolean examples

print(1 < 2)
print(1 < 1)
print(1 == 1)
print(1 >= 1)
